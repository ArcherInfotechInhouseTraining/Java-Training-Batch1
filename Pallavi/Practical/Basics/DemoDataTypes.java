class DemoDataTypes
{
	public static void main(String []args)
	{
		int x=123;
		byte b=10;
		char c='$';
		float f=32.42F;
		double db=546.789;
		boolean b1=true;

		System.out.println("x : "+x);
		System.out.println("b : "+b);
		System.out.println("c : "+c);
		System.out.println("f : "+f);
		System.out.println("db : "+db);
		System.out.println("b1 : "+b1);
	}
}